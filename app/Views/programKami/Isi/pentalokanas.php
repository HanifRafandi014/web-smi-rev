      <div class="section-header" style="padding-top:4%;">
        <h2>Peserta PENTALOKANAS</h2>
      </div>
      <p>Kegiatan PENTALOKANAS ini layak anda ikuti apabila</p>
      <ol>
        <li><b>Yayasan Pendidikan</b>: Peserta yang berasal dari yayasan pendidikan yang memiliki tanggung jawab langsung terhadap pengelolaan lembaga pendidikan, baik itu sekolah, lembaga kursus, maupun institusi pendidikan lainnya. </li>
        <li><b>Pengelola Lembaga Pendidikan</b>: Peserta yang merupakan pengelola atau pimpinan lembaga pendidikan, seperti kepala sekolah, kepala lembaga kursus, kepala pusat pendidikan, atau posisi sejenis. </li>
        <li><b>Guru dan Tenaga Kependidikan</b>: Peserta yang merupakan guru atau tenaga kependidikan yang aktif bekerja di lembaga pendidikan yang diwakilinya, termasuk staf administrasi, kepala bidang akademik, kepala bidang non-akademik, dan posisi sejenis. </li>
        <li><b>Pemegang Kebijakan Pendidikan</b>: Peserta yang memiliki peran dalam menetapkan kebijakan pendidikan di lembaga atau yayasan pendidikan, seperti anggota dewan pendidikan, pengurus yayasan, atau posisi sejenis. </li>
        <li><b>Penggiat Pendidikan</b>: Peserta yang memiliki minat dan keterlibatan aktif dalam pengembangan pendidikan, meskipun tidak secara langsung terlibat dalam pengelolaan lembaga pendidikan. </li>
        <li><b>Pendidikan Formal atau Non-Formal</b>: Peserta yang berasal dari lembaga pendidikan formal (seperti sekolah, perguruan tinggi, atau lembaga pendidikan formal lainnya) maupun lembaga pendidikan non-formal (seperti lembaga kursus, pusat pelatihan, atau komunitas pendidikan). </li>
        <li><b>Minat dalam Peningkatan Kualitas Pendidikan</b>: Peserta yang memiliki minat dan komitmen untuk terus meningkatkan kualitas pendidikan di lembaga atau yayasan pendidikan yang mereka wakili. </li>
      </ol>
      <p>Dengan mempertimbangkan kriteria-kriteria di atas, peserta yang memenuhi syarat dapat mengikuti kegiatan PENTALOKANAS untuk mendapatkan manfaat yang maksimal dalam upaya meningkatkan kualitas pendidikan di lembaga mereka. </p>
